package com.wxjf.sdk.faceplusplus.model;

public class HeadRect {

	private Coordinate lt;
	private Coordinate rt;
	private Coordinate rb;
	private Coordinate lb;

	public Coordinate getLt() {
		return lt;
	}

	public void setLt(Coordinate lt) {
		this.lt = lt;
	}

	public Coordinate getRt() {
		return rt;
	}

	public void setRt(Coordinate rt) {
		this.rt = rt;
	}

	public Coordinate getRb() {
		return rb;
	}

	public void setRb(Coordinate rb) {
		this.rb = rb;
	}

	public Coordinate getLb() {
		return lb;
	}

	public void setLb(Coordinate lb) {
		this.lb = lb;
	}

}
